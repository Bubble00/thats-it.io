const messages = [
    "Are you sure?",
    "Really sure??",
    "Are you positive?",
    "Pookie please...",
    "Just think about it!",
    "If you say no, I will be really sad...",
    "I will be very sad...",
    "I will be very very very sad...",
    "Ok fine, I will stop asking...",
    "Just kidding, say yes please! ❤️"
];

let messageIndex = 0;

function handleNoClick() {
    const noButton = document.querySelector('.no-button');
    const yesButton = document.querySelector('.yes-button');
    noButton.textContent = messages[messageIndex];
    messageIndex = (messageIndex + 1) % messages.length;
    const currentSize = parseFloat(window.getComputedStyle(yesButton).fontSize);
    yesButton.style.fontSize = `${currentSize * 1.5}px`;
}

function handleYesClick() {
    window.location.href = "yes_page.html";
}

function submitName() {
    const nameInput = document.getElementById('name-input').value.trim(); // Get the input value and trim whitespace
    if (nameInput === 'Marshmallow') { // Check if the input matches "Marshmallow"
        document.getElementById('greeting').textContent = `Hey ${nameInput}, Saturday is around the corner and you know what day that is...`;
        document.getElementById('name-prompt').style.display = 'none'; // Hide the name prompt
        document.getElementById('valentine-message').style.display = 'block'; // Show the Valentine message
    } else {
        alert("That's not the correct name. Please try again."); // Alert if the input is not "Marshmallow"
    }
}

function createHeart() {
    const heartContainer = document.getElementById('heart-container');
    const heart = document.createElement('div');
    heart.classList.add('heart');

    // Randomize the position and animation duration
    heart.style.left = `${Math.random() * 100}vw`;
    heart.style.animationDuration = `${Math.random() * 2 + 3}s`;

    heartContainer.appendChild(heart);

    // Remove the heart after the animation ends
    setTimeout(() => {
        heart.remove();
    }, 5000);
}

// Generate hearts at intervals
setInterval(createHeart, 300);